public class WhileDemo {
    public static void main(String[] args) {
        int i=0;
        while(i<5){
            System.out.println("asd"+i);
            i++;
        }

        double zu=8848860;
        double zi=0.1;
        int nub=0;
        while (zi<=zu){
            nub++;
            zi=zi*2;
        }
        System.out.println(nub);
        System.out.println(zi);

    }
}
